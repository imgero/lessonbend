import { mkdir, writeFile } from "node:fs/promises";
import { createClient } from "@libsql/client";

const db = createClient({ url: "file:lessonbend.db" });
const lessons = [
  {
    id: "fractions",
    title: "Equivalent fractions",
    description: "Equal parts, visual models, and quick feedback.",
    artifacts: [
      ["short-concrete-loops", "Pineapple", "19a7cf5d-e757-45c6-9339-4efb9dfc0f60"],
      ["audio-first", "Blueberry", "f2507019-4352-4bfa-9075-49c51b99b9c8"],
      ["math-language-support", "Mango", "df283f2c-a932-4730-8062-ba9b8229ec2b"],
    ],
  },
  {
    id: "science",
    title: "States of water",
    description: "Observe, connect, and explain solids, liquids, and gases.",
    artifacts: [
      ["short-concrete-loops", "Pineapple", "d073644b-dc77-4902-a598-4e6059826459"],
      ["audio-first", "Blueberry", "7df7548a-f7dd-46f2-8f6e-8c1ef58759c6"],
      ["math-language-support", "Mango", "745c48a5-ffc3-44f9-9423-647c82ef5d62"],
    ],
  },
  {
    id: "reading",
    title: "Hedgehog reading",
    description: "A short passage with playful comprehension checks.",
    artifacts: [["short-concrete-loops", "Pineapple", "ce26c0a0-f99f-4124-8751-a6be74d8bac8"]],
  },
];

const records = await Promise.all(
  lessons.flatMap((lesson) => lesson.artifacts.map(async ([profileId, label, id]) => {
    const result = await db.execute({ sql: "SELECT html FROM artifacts WHERE id = ?", args: [id] });
    const html = result.rows[0]?.html;
    if (typeof html !== "string") throw new Error(`Missing artifact ${id}`);
    return [id, { id, profileId, label, html }];
  })),
);
const byId = new Map(records);
const gallery = lessons.map((lesson) => ({
  ...lesson,
  artifacts: lesson.artifacts.map(([, , id]) => byId.get(id)),
}));
const source = `// Generated by scripts/export-static-gallery.mjs. Do not hand-edit.\nexport type StaticGalleryArtifact = { id: string; profileId: string; label: string; html: string };\nexport type StaticGalleryLesson = { id: string; title: string; description: string; artifacts: StaticGalleryArtifact[] };\nexport const staticGallery: StaticGalleryLesson[] = ${JSON.stringify(gallery)};\n`;
await mkdir("src/lib", { recursive: true });
await writeFile("src/lib/static-gallery.ts", source);
console.log(`Exported ${records.length} artifacts (${Math.round(Buffer.byteLength(source) / 1024)} KB).`);
